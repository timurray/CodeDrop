public class StaircasePrinter
{
	public static void main(String[] args)
	{
		//Shane Butt 200621753
		//A Kitty Just For Giggles
		System.out.println("            (| /) ");
		System.out.println("            ('.') ");
		System.out.println("             (O)  ");

		//Top Stair
		System.out.println("            +---+");
		System.out.println("            |   |");

		//Third Stair
		System.out.println("        +---+---+");
		System.out.println("        |   |   |");

		//Second Stair
		System.out.println("    +---+---+---+");
		System.out.println("    |   |   |   |");

                //Bottom Stair
		System.out.println("+---+---+---+---+");
		System.out.println("|   |   |   |   |");
		System.out.println("+---+---+---+---+");
	}
}
